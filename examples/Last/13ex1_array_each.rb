# An array is an ordered list of elements that can be of any type.
# You can define an array by placing a list of elements between brackets

# ex: [1, 'Bob', 4.33, 'another string']

# A variable can hold only one item at a time.
# Arrays can hold multiple items.
# These items are called elements of the array.
# Arrays can hold objects of any data type.
# Each element can be referred to by an index.
# Arrays are zero based.
# The index of the first element is zero.

nums = [1, 2, 3, 4, 5]
nums.each do |num|
	puts num
end

# Above example will create an array of five integers.
# The elements of the array are printed to the console.
