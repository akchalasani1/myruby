# Hash creation with literal notation => :

# In the third script, we create a hash with the "hash literal notation".
# The "values" are bound by the "curly brackets".
# And the "key-value" pairs are associated with the "=>" characters.

domains = {"de" => "Germany",
		"us" => "United States",
					 "ca" => "Canada",
					 "ind" => "India",
					 "no" => "Norway"


}

puts domains

# We create a domains hash with 5 pairs. This time both "keys" and "values" are "string types".

